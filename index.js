var product = num1 * num2;
console.log(product);
// exponent (raise to power ^)
var exponent = 2 ** 5;
console.log(exponent);
// division
var division = num2 / num1;
console.log(division);
//division (remainder)
var remainder = num2 / 3;
console.log(remainder);
